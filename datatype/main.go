package main

import "fmt"

func main() {
	var a bool = true        // boolean
	var b int = 5            // integer
	var c int = -6           // signed integer
	var d uint = 7           // unsigned integer
	var e float32 = 3.14     // float
	var f float64 = 1.7e+308 // double
	var g string = "abc"     // string

	fmt.Println(a)
	fmt.Println(b)
	fmt.Println(c)
	fmt.Println(d)
	fmt.Println(e)
	fmt.Println(f)
	fmt.Println(g)
}